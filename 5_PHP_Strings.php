//==============================================  ===============================   ================================================
//==============================================  strrev() - Reverse a String end   ================================================
//==============================================  ===============================   ================================================










//==============================================  ============================================   ================================================
//==============================================  strpos() - Search For a Text Within a String   ================================================
//==============================================  ============================================   ================================================
/* The PHP strpos() function searches for a specific text within a string. If a match is found, 
