echo '<br><br>'.'strpos() - Search For a Text Within a String : '.strpos("Hello world!", "world"); // outputs 6

//==============================================  ================================================   ================================================
//==============================================  strpos() - Search For a Text Within a String end   ================================================
//==============================================  ================================================   ================================================













